    public string Current { get; private set; } = "de";
    public event Action? Changed;

    public void Set(string lang)
    {
        if (Trans.ContainsKey(lang) && lang != Current)
        {
            Current = lang;
            Changed?.Invoke();
        }
    }

    public string this[string key]
        => Trans[Current].TryGetValue(key, out var v) ? v
           : Trans["de"].TryGetValue(key, out var fb) ? fb
           : key;
